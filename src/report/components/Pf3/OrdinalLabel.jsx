// اعداد ۱ تا ۹ (ساده)
const UNITS_SIMPLE = ['', 'اول', 'دوم', 'سوم', 'چهارم', 'پنجم', 'ششم', 'هفتم', 'هشتم', 'نهم'];
// برای ترکیب (بیست‌ویکم ← «یکم» لازم است)
const UNITS_COMPOUND = ['', 'یکم', 'دوم', 'سوم', 'چهارم', 'پنجم', 'ششم', 'هفتم', 'هشتم', 'نهم'];

const TEENS = {
  10: 'دهم', 11: 'یازدهم', 12: 'دوازدهم', 13: 'سیزدهم', 14: 'چهاردهم',
  15: 'پانزدهم', 16: 'شانزدهم', 17: 'هفدهم', 18: 'هجدهم', 19: 'نوزدهم'
};

const TENS_CARD = { 2: 'بیست', 3: 'سی', 4: 'چهل', 5: 'پنجاه', 6: 'شصت', 7: 'هفتاد', 8: 'هشتاد', 9: 'نود' };
const TENS_ORD  = { 20: 'بیستم', 30: 'سی\u200cام', 40: 'چهلم', 50: 'پنجاهم', 60: 'شصتم', 70: 'هفتادم', 80: 'هشتادم', 90: 'نودم', 100: 'صدم' };

// تبدیل عدد → ترتیب فارسی
 const toOrdinalFa = (n) => {
  n = Number(n);
  if (!Number.isFinite(n) || n < 1) return '';
  if (n <= 9) return UNITS_SIMPLE[n];
  if (n >= 10 && n <= 19) return TEENS[n];
  if (n % 10 === 0) return TENS_ORD[n] || '';
  if (n === 100) return 'صدم';

  const t = Math.floor(n / 10);
  const u = n % 10;
  return `${TENS_CARD[t]}\u200cو${UNITS_COMPOUND[u]}`; // «بیست‌ویکم»، «هفتاد‌وسوم»، ...
};

// برچسب نهایی با «مرتبه»
export const ordinalLabel = (n) => n ? `مرتبه ${toOrdinalFa(n)}` : '';
